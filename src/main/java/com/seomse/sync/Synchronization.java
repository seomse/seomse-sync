/**
 * 자동 동기화 annotation
 * annotation 이 지정 되면 SynchronizerManager 에서 자동 호출 됨
 * @author macle
 */
@Target({ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
public @interface Synchronization {
}
